package _13_array;

public class Demo5 {
    public static void main(String[] args) {
        int[] array = {10, 20, 30, 40, 50};
        //print array elements using for loop
        for (int index = 0; index < array.length; index++) {
            System.out.println(index + "\t ---> " + array[index]);
        }

    }
}
